    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String message ="Do you really want to  delete this record? ";
        int reply=JOptionPane.showConfirmDialog(null, message, "Delete",JOptionPane.YES_NO_CANCEL_OPTION);
        
        if(reply==JOptionPane.YES_OPTION){
            if(ID>0){
                try {
                String delque = "DELETE FROM products WHERE id  =? " ;
                pst = (PreparedStatement) Conn.prepareStatement(delque);
                pst.setInt(1, ID);
                pst.execute();
                Table();
                
                
            } catch (SQLException ex) {
                Logger.getLogger(InternalManage.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
            else{
                JOptionPane.showMessageDialog(null,"NOT Selected row.Try again...");
                
            }
        }
            
    }//GEN-LAST:event_jButton2ActionPerformed

