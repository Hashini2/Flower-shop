        double total =  Double.parseDouble(jTextField5.getText());
        int qty =Integer.parseInt( jTextField3.getText());
        
        try {
               // Coection
               Connection Conn = DataBaseCon.connect();
               String insertSql ="INSERT INTO products(flower,categ,qty,price,total) VALUES (?,?,?,?,?)";
               java.sql.PreparedStatement pst = (java.sql.PreparedStatement) Conn.prepareStatement(insertSql);
               
               pst.setString(1, getflower);
               pst.setString(2, getcateg);
               pst.setInt(3, qty);
               pst.setDouble(4, price);
               pst.setDouble(5, total);
               
               
               pst.executeUpdate();
               JOptionPane.showMessageDialog(null,"Successful ");
               
               
               
           } catch (SQLException ex) {
               Logger.getLogger(Internal_Insert.class.getName()).log(Level.SEVERE, null, ex);
           }
          
